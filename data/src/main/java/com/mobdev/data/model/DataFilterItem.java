package com.mobdev.data.model;

public class DataFilterItem {
    private String value;
    private int type;

    public DataFilterItem() {
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
